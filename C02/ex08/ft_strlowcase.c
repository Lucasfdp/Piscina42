char	*ft_strlowcase(char *str)
{
	char	*lower;

	lower = str;
	while (*str)
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str = *str + 32;
		}
		str++;
	}
	return (lower);
}
